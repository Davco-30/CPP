#include <iostream>

using namespace std;
///Omkar and Completion - Codeforces

int main()
{
    int t; ///Number of test cases
    cin >> t;

    for(int i = 0; i < t; i++){
        int n; ///New Case
        cin >> n;

        int arr[n];

        for(int i = 0; i < n; i++){
            arr[i] = 1;
            cout << arr[i] << " ";
        }

        cout << "\n";

    }



    return 0;
}
